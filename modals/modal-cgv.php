      <p class="mb-4">
        Si les clés ne sont pas remises lors de la signature du contrat, le propriétaire s’engage à les fournir au minimum 24 heures avant son départ, à l’endroit sécurisé mentionné dans le contrat. Nat'Patoune décline toute responsabilité en cas de perte, vol ou disparition de clés déposées par le client dans un lieu non sécurisé ou non conforme aux indications convenues.
