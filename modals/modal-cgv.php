        <li>le carnet de vaccination (ou passeport de l’animal),</li>
        <li>les coordonnées du vétérinaire habituel.</li>
      </ul>
      <p class="mb-2">En cas d’urgence médicale :</p>
      <ul class="list-disc pl-5 mb-4 space-y-1">
