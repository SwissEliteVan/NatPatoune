      <ul class="list-disc pl-5 mb-4 space-y-1">
        <li>Informer le propriétaire avant de prendre rendez-vous, dans la mesure permise par la situation ;</li>
        <li>Le tenir régulièrement informé de l’évolution de l’état de santé de l’animal ;</li>
        <li>Agir dans l’intérêt du bien-être et de la sécurité du chat.</li>
      </ul>
