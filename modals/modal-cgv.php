      </p>

      <p class="font-bold mb-2">8. Sécurité du domicile et fugue</p>
      <p class="mb-2">
        Avant son départ, le propriétaire s’engage à vérifier et sécuriser l’environnement du chat afin d’éliminer tout danger potentiel, tels que :
      </p>
      <ul class="list-disc pl-5 mb-4 space-y-1">
        <li>fenêtres laissées en imposte ou accessibles,</li>
        <li>balcons ou terrasses non sécurisés,</li>
        <li>cuvettes de WC ouvertes,</li>
        <li>fils, cordons, ficelles ou objets dangereux,</li>
        <li>plantes toxiques,</li>
        <li>ou tout autre risque non mentionné mais pouvant compromettre la sécurité de l’animal.</li>
      </ul>
      <p class="mb-4">
        Nat'Patoune décline toute responsabilité en cas d'incident lié à un environnement non sécurisé.
      </p>
      <p class="mb-4">
        Nat'Patoune décline également toute responsabilité en cas de fugue de l’animal durant une prestation à domicile. Toutefois, Nat'Patoune s’engage à entreprendre toutes les démarches raisonnables et nécessaires pour rechercher l’animal en l’absence du propriétaire (tour du quartier, appels, messages aux voisins, etc.).
      </p>

      <p class="font-bold mb-2">9. Refus de prise en charge pour raisons de santé</p>
      <p class="mb-4">
        Nat'Patoune se réserve le droit de refuser de prendre en charge un animal présentant une maladie grave ou une maladie contagieuse. Cette mesure vise à protéger la santé de l’animal, ainsi que celle des autres animaux et du personnel de Nat'Patoune.
      </p>

      <p class="font-bold mb-2">10. Manquement grave aux soins et protection des animaux</p>
      <p class="mb-4">
        En cas de sérieux manquement aux soins des animaux, de logement insalubre ou de toute autre situation de négligence, conformément à l’art. 3 de la LPA (Loi fédérale sur la protection des animaux), Nat'Patoune se réserve le droit d’aviser le vétérinaire cantonal.
      </p>
      <p class="mb-4">
        Le vétérinaire cantonal décidera des mesures nécessaires pour assurer le bien-être et la sécurité des animaux.
      </p>

      <p class="font-bold mb-2">11. Photos et vidéos</p>
      <p class="mb-4">
        Nat'Patoune peut, avec l’autorisation écrite du propriétaire (formulaire ou mention dans le contrat), prendre des photos ou vidéos de l’animal pour diffusion sur les supports suivants : site internet, Facebook, Instagram ou autres médias mentionnés dans le contrat.
      </p>
      <p class="mb-4">
        Le propriétaire peut refuser cette autorisation. Dans ce cas, seules des photos nécessaires seront prises pour être envoyées au propriétaire durant son absence.
      </p>
      <p class="mb-2">Nat'Patoune s’engage à :</p>
      <ul class="list-disc pl-5 mb-4 space-y-1">
        <li>publier uniquement des photos ou vidéos des animaux,</li>
        <li>ne jamais porter atteinte à la vie privée des propriétaires ou de leur domicile.</li>
      </ul>

      <p class="font-bold mb-2">12. Arrosage des plantes</p>
      <p class="mb-4">
        Sur demande du propriétaire, Nat'Patoune peut arroser les plantes selon les instructions détaillées indiquées dans le contrat.
      </p>
      <p class="mb-2">Le propriétaire est informé que :</p>
      <ul class="list-disc pl-5 mb-4 space-y-1">
        <li>les animaux restent la priorité lors de chaque visite,</li>
        <li>Nat'Patoune décline toute responsabilité si une plante n’a pas reçu la quantité d’eau adéquate ou si elle est inaccessible,</li>
        <li>Nat'Patoune ne monte pas sur les meubles pour arroser les plantes placées en hauteur.</li>
      </ul>

      <p class="font-bold mb-2">13. Frais de stationnement</p>
      <p class="mb-4">
        Si aucun stationnement gratuit n’est disponible aux alentours du domicile, Nat'Patoune se réserve le droit de demander une participation aux frais de parking, fixée à CHF 2.– par visite.
      </p>
      <p class="mb-4">
        Ces frais ne sont pas facturés les dimanches et jours fériés, sauf si le domicile du propriétaire se situe dans une zone où le stationnement est payant 7 jours sur 7.
      </p>

      <p class="font-bold mb-2">14. Hygiène et nettoyage</p>
      <p class="mb-2">Le propriétaire s’engage à mettre à disposition de Nat'Patoune :</p>
      <ul class="list-disc pl-5 mb-4 space-y-1">
        <li>un savon et une serviette pour le lavage des mains à l’arrivée, au départ et après le nettoyage de la litière, afin de limiter tout risque de contamination d’un animal à un autre,</li>
        <li>un évier accessible et fonctionnel pour le nettoyage quotidien des gamelles et autres ustensiles liés aux soins des animaux.</li>
      </ul>

      <p class="font-bold mb-2">15. Modification des tarifs</p>
      <p class="mb-4">
        Nat'Patoune se réserve le droit de modifier les tarifs de ses prestations à tout moment.
      </p>
      <ul class="list-disc pl-5 mb-2 space-y-1">
        <li>Les modifications n’ont aucun effet sur les gardiennages déjà en cours.</li>
        <li>Le propriétaire sera informé de tout changement par email ou autre moyen de communication convenu.</li>
        <li>Les nouveaux tarifs s’appliqueront à la réservation suivante. Les tarifs en vigueur sont consultables sur le site internet de Nat'Patoune.</li>
      </ul>
